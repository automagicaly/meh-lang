package io.lorde;

public enum TokenType {
    // operators
    PLUS, MINUS, SLASH, STAR, EQUALS, ARROW, COLON, OPEN_PARENTHESIS, CLOSE_PARENTHESIS, OPEN_SQUARE_BRACKET,
    CLOSE_SQUARE_BRACKET, EQUAL_TO, LESS_THAN, GREATER_THAN, LESS_OR_EQUAL_TO, GREATER_OR_EQUAL_TO, NOT_EQUAL_TO,
    AMP, PIPE, SHIFT_LEFT, SHIFT_RIGHT, COMMA, VOID, BOOL, PERCENT, ANY,

    // literals:w

    IDENTIFIER, NUMBER_INTEGER, NUMBER_HEX_INTEGER, NUMBER_FLOAT, NEW_LINE, STRING, BYTE, FLOAT, DOUBLE, FILE, AS, IS,
    INTEGER,

    // keywords
    LET, BACK_SLASH, RETURN, IF, ELSE, AND, OR, NOT, TRUE, FALSE,

    // others
    SPACE, EOF
}
